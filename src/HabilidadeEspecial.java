public class HabilidadeEspecial {

    String nome;
    double custoEnergia;
    boolean habilitada;

    public HabilidadeEspecial (String nome, double custoEnergia, boolean habilitada) {
        this.nome = nome;
        this.custoEnergia = custoEnergia;
        this.habilitada = habilitada;
    }

}
